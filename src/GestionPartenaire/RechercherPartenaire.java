    private void bRechercherActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bRechercherActionPerformed

        if (cTypePartenaire.getSelectedIndex() == -1 && cWillayaPartenaire.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(null, "Enter Une des combinaison suivante:"
                    + "\n     - Type Partenaire."
                    + "\n     - Nom Partenaire."
            + "\n     - Type Partenaire + Nom Partenaire.");

        } else if ( cWillayaPartenaire.getSelectedIndex() == -1) {
            try {
                con = Connect.connect();
                String sql = "SELECT * FROM partenaire WHERE Type_p = '"+ cTypePartenaire.getSelectedItem().toString() +"'";
                pst = con.prepareStatement(sql);
                rst = pst.executeQuery(sql);
                tPartenaire.setModel(DbUtils.resultSetToTableModel(rst));
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        } else if (cTypePartenaire.getSelectedIndex() == -1 ) {
            try {
                con = Connect.connect();
                String sql = "SELECT * FROM partenaire WHERE  willaya_p='"+ cWillayaPartenaire.getSelectedItem()+"'";
                pst = con.prepareStatement(sql);
                rst = pst.executeQuery(sql);
                tPartenaire.setModel(DbUtils.resultSetToTableModel(rst));
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        } else if (!(cTypePartenaire.getSelectedIndex() == -1) && !(cWillayaPartenaire.getSelectedIndex() == -1)) {
            try {
                con = Connect.connect();
                String sql = "SELECT * FROM partenaire WHERE  willaya_p='"+ cWillayaPartenaire.getSelectedItem()
                        +"' AND Type_p = '"+cTypePartenaire.getSelectedItem() +"'";
                pst = con.prepareStatement(sql);
                rst = pst.executeQuery(sql);
                tPartenaire.setModel(DbUtils.resultSetToTableModel(rst));
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
    }//GEN-LAST:event_bRechercherActionPerformed

    private void tPartenaireMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tPartenaireMouseClicked
        
        con = Connect.connect();
        int row = tPartenaire.getSelectedRow();    
        String n;     
        n = tPartenaire.getModel().getValueAt(row, 0).toString();
         
        try {
            String sql = "SELECT * FROM partenaire WHERE id_p = '" + n + "'";
             
            pst = con.prepareStatement(sql);
             
            rs = pst.executeQuery(sql);
           
             
            if (rs.next()) {
                 
                this.setVisible(false);
                partenaire s1 = new partenaire();
                s1.setVisible(true);
                String telephone_p = rs.getString("telephone_p");
                s1.txtTelPartenaire.setText(telephone_p);
                String nom_p = rs.getString("nom_p");
                s1.txtNomPartenaire.setText(nom_p);
                String type_p = rs.getString("type_p");
                s1.cTypePartenaire.setSelectedItem(type_p);
                String willaya_p = rs.getString("willaya_p");
                s1.cWillayaPartenaire.setSelectedItem(willaya_p);
                s1.id_p = rs.getInt("id_p");
                
                s1.bModifierPartenaire.setEnabled(true);
                s1.bSavePartenaire.setEnabled(false);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
       
    }//GEN-LAST:event_tPartenaireMouseClicked

