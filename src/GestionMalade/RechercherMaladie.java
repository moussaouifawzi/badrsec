    private void tUserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tUserMouseClicked
        
        con = Connect.connect();
        int row = tUser.getSelectedRow();
//        System.out.println("1");
        String n;
        
        n = tUser.getModel().getValueAt(row, 1).toString();
//         System.out.println("11");
        try {
            String sql = "SELECT * FROM maladies WHERE 	id_maladi = '" + n + "'";
//             System.out.println("3");
            pst = con.prepareStatement(sql);
//             System.out.println("4");
            rs = pst.executeQuery(sql);
//             System.out.println("5"+rs.toString());
             
            if (rs.next()) {
//                 System.out.println("6");
                this.setVisible(false);
                Maladies s2 = new Maladies();
                s2.setVisible(true);
                
                String Type_cancer = rs.getString("Type_cancer");
                s2.txtMaladie.setText(Type_cancer);
                
                s2.id_maladi = rs.getInt("id_maladi");
                s2.bModifier.setEnabled(true);
                s2.bAjouter.setEnabled(false);
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
            log.error(e);
        }
       
    }//GEN-LAST:event_tUserMouseClicked

